  // „Çø„Ç§„É†„Ç¢„Ç¶„Éà‰ªò„ÅçÂÆüË°å
  private async executeWithTimeout<T>(
    fn: () => Promise<T>,
    timeout: number,
    timeoutMessage: string,
  ): Promise<T> {
    return new Promise<T>((resolve, reject) => {
      const timer = setTimeout(() => {
        reject(new Error(timeoutMessage));
      }, timeout);

      fn().then(
        (result) => {
          clearTimeout(timer);
          resolve(result);
        },
        (error) => {
          clearTimeout(timer);
          reject(error);
        },
      );
    });
  }

  // „É™„Éà„É©„Ç§‰ªò„ÅçÂÆüË°å
  private async executeWithRetry<T>(
    fn: () => Promise<T>,
    serviceName: string,
    maxRetries: number,
    logger: Logger,
  ): Promise<T> {
    let lastError: Error | null = null;

    for (let attempt = 0; attempt <= maxRetries; attempt++) {
      try {
        // ÂàùÂõû‰ª•Â§ñ„ÅØ„É™„Éà„É©„Ç§„É°„ÉÉ„Çª„Éº„Ç∏„Çí„É≠„Ç∞
        if (attempt > 0) {
          logger.warn(`„É™„Éà„É©„Ç§ÂÆüË°å ${attempt}/${maxRetries}`);
          await this.commonLog.log(
            "warn",
            `„É™„Éà„É©„Ç§ÂÆüË°å ${attempt}/${maxRetries}`,
            `ASP:${serviceName}`,
          );
        }

        return await fn();
      } catch (error) {
        lastError = error instanceof Error ? error : new Error(String(error));

        // ÊúÄÂæå„ÅÆ„É™„Éà„É©„Ç§„Åß„Å™„Åë„Çå„Å∞ÂæÖÊ©ü„Åó„Å¶„É™„Éà„É©„Ç§
        if (attempt < maxRetries) {
          const waitTime = 3000; // 3ÁßíÂæÖÊ©ü
          logger.warn(
            `„Ç®„É©„ÉºÁô∫Áîü: ${lastError.message} - ${waitTime}msÂæå„Å´„É™„Éà„É©„Ç§„Åó„Åæ„Åô`,
          );
          await new Promise((resolve) => setTimeout(resolve, waitTime));
        }
      }
    }

    // „Åô„Åπ„Å¶„ÅÆ„É™„Éà„É©„Ç§„ÅåÂ§±Êïó„Åó„ÅüÂ†¥Âêà
    throw lastError || new Error(`${serviceName}„ÅÆÂÆüË°å„Å´Â§±Êïó„Åó„Åæ„Åó„Åü`);
  }
